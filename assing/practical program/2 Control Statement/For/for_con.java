class for_con
{                                 
        public static void main(String args[])
        {                                             
                int i,j,k;  

                for(i=1;i<=10;i++)
                {
                        for(j=1;j<=10;j++)
                        {
                                k=i*j;
                                System.out.print(k);
                                System.out.print("\t");
                        }
                        System.out.print("\n");
                }
        }
}



