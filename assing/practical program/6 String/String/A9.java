                             /* Character Extraction by "charAt()" method */


class A9
{
        public static void main(String args[])
        {
                String s="sagar k.";

                char ch=s.charAt(4);

                System.out.println("Character At :" + ch);
                System.out.println("Character At :" + s.charAt(4));

                System.out.println("Character At :" + ("sagar k" . charAt(4) ) );
        }
}
