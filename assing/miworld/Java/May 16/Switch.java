class Switch
{
  public static void main(String args[])
  {
   int a=2;
   switch(a)
    {
     case 1:
            System.out.println("Hello");
     case 2:
            System.out.println("Hi");
     case 3:
            System.out.println("Bye");
     default:
            System.out.println("None");
    }
  }
}