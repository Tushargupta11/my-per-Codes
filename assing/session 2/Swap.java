class Swap
 { 
  public static void main(String args[])
  {
  int x=3,y=5;
 System.out.println("$$$$$ values before swapping $$$$$");
 System.out.println(x+" "+y);
  x=x+y;
  y=x-y;
  x=x-y;
 System.out.println("$$$$$ values after swapping $$$$$");
 System.out.println(x+" "+y);
 System.out.println("values succesfully swapped");
  }
}

  
